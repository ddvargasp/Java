    private void cargarDatosVehiculo(int idVehiculo) {
        // Busqueda de vehiculo en DB
        vehiculo = controladora.listarVehiculos(idVehiculo);
        
        // Asignación de valores
        txtModelo.setText(vehiculo.getModelo());
        txtMarca.setText(vehiculo.getMarca());
        txtMotor.setText(vehiculo.getMotor());
        txtColor.setText(vehiculo.getColor());
        txtPatente.setText(vehiculo.getPatente());
        txtCantidadPuertas.setText(String.valueOf(vehiculo.getCantidadPuertas()));
    }
    
    public void mostrarMensaje (String mensaje, String tipo, String titulo){
        
        JOptionPane optionPane = new JOptionPane(mensaje);
        
        // Mensaje informativo
        if(tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        
        // Mensaje Error
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
