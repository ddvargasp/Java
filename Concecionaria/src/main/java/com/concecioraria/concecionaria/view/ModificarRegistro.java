    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        String modelo = txtModelo.getText();
        String marca = txtMarca.getText();
        String motor = txtMotor.getText();
        String color = txtColor.getText();
        String patente = txtPatente.getText();
        int cantidadPuertas = Integer.parseInt(txtCantidadPuertas.getText());
        
        controladora.modificarVehiculo(vehiculo, modelo, marca, motor, color, patente, cantidadPuertas);
        
        mostrarMensaje ("Modificación realizada correctamente", "Info", "Modificación exitosa");
        
        ConsultarRegistro consultarRegistro = new ConsultarRegistro();
        consultarRegistro.setVisible(true);
        consultarRegistro.setLocationRelativeTo(null);
        
        this.dispose();

    }//GEN-LAST:event_btnModificarActionPerformed

